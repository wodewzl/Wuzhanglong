public class LogoVO {

    private String code;
    private LogoVO datas;
    private String img;
    private String url;
    private String title;

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public LogoVO getDatas() {
        return datas;
    }

    public void setDatas(LogoVO datas) {
        this.datas = datas;
    }

    public String getImg() {
        return img;
    }

    public void setImg(String img) {
        this.img = img;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }
}
